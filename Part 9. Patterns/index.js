/* 
  Существует метод test, который принимает ссылку типа List (с
  любой допустимой параметризацией). Содержание данного метода для работы
  несущественно, выберем логирующую заглушку.

  Вызовите метод test, передав туда список с пятью числами.

  После завершения метода test выведите в лог число —
  количество, сколько раз (в сумме) вызывались любые перегрузки метода add.

  Существенным условием является то, что test не может и не
  должен знать ничего о сути задачи.
*/

// Создаём список с пятью числами
const numbersList = [1, 2, 3, 4, 5];

// Определяем счётчик вызовов метода add
let addCallCount = 0;

// Переопределяем метод add у прототипа Array для логирования вызовов
const originalAdd = Array.prototype.push; // используем push вместо add, так как в JS у массива нет метода add
Array.prototype.push = function(...args) {
  addCallCount += args.length;

  return originalAdd.apply(this, args); // вызываем оригинальный метод push
};

// Определяем метод test, который принимает список (массив)
function test(list) {
  /* 
    тут может быть какая-то логика, но она нам не важна в данной задаче
  */
  
  // добавляем два элемента — это вызовет наш переопределённый push 2 раза (чтобы протестировать логирование)
  list.push(6, 7);
}

test(numbersList);

console.log(`Количество вызовов метода push (add): ${addCallCount}`);
/* 
  Вывод в консоли:
  Количество вызовов метода push (add): 2
*/
